    /**
     * Light RAG Search: Scores FAQs based on word overlaps with query.
     */
    fun searchFaqs(query: String): List<FaqItem> {
        if (query.isBlank()) return emptyList()
        val queryWords = query.lowercase()
            .replace("؟", "")
            .replace("?", "")
            .split("\\s+".toRegex())
            .filter { it.length > 2 } // filter out small stop words

        if (queryWords.isEmpty()) {
            // fallback to simple substring matching
            return faqs.filter { it.question.contains(query, ignoreCase = true) || it.answer.contains(query, ignoreCase = true) }
        }

        return faqs.map { faq ->
            var score = 0
            // Score based on keyword matches
            faq.keywords.forEach { keyword ->
                if (query.contains(keyword, ignoreCase = true)) {
                    score += 4
                }
            }
            // Score based on word overlaps in question and answer
            queryWords.forEach { word ->
                if (faq.question.contains(word, ignoreCase = true)) {
                    score += 2
                }
                if (faq.answer.contains(word, ignoreCase = true)) {
                    score += 1
                }
            }
            faq to score
        }
        .filter { it.second > 0 }
        .sortedByDescending { it.second }
        .map { it.first }
    }
